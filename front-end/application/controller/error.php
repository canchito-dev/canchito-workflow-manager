namespace Application\Controller;

use Application\Core\Controller;

/**
 * Class Error
 *
 * Please note:
 * Don't use the same name for class and method, as this might trigger an (unintended) __construct of the class.
 * This is really weird behaviour, but documented here: http://php.net/manual/en/language.oop5.decon.php
 * 
 * The model is not loaded in the base Controller class inside the constructor because there might be functions
 * or procedures that might not need the model. If we automatically load it, we will be loading resources that
 * might not be used in the end
 *
 **/
class Error extends Controller {
    /**
     * PAGE: index
     * This method handles the error page that will be shown when a page is not found
     **/
    public function index() {
        // load views
    	require APP . 'view/_templates/header.php';
    	require APP . 'view/_templates/nav.header.php';
        require APP . 'view/error/index.php';
    	require APP . 'view/_templates/footer.php';
    }
}
