package com.canchitodev.cwm.domain;

import java.io.Serializable;

public class FileHandler implements Serializable {

	private static final long serialVersionUID = -4949197681431202005L;

	private String name;
	private String filename;
	private FolderHandler folderHandler;
	
	public FileHandler() {}

	public FileHandler(String name, String filename, FolderHandler folderHandler) {
		this.name = name;
		this.filename = filename;
		this.folderHandler = folderHandler;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public FolderHandler getFolderHandler() {
		return folderHandler;
	}

	public void setFolderHandler(FolderHandler folderHandler) {
		this.folderHandler = folderHandler;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((filename == null) ? 0 : filename.hashCode());
		result = prime * result + ((folderHandler == null) ? 0 : folderHandler.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FileHandler other = (FileHandler) obj;
		if (filename == null) {
			if (other.filename != null)
				return false;
		} else if (!filename.equals(other.filename))
			return false;
		if (folderHandler == null) {
			if (other.folderHandler != null)
				return false;
		} else if (!folderHandler.equals(other.folderHandler))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "FileHandler [name=" + name + ", filename=" + filename + ", folderHandler=" + folderHandler + "]";
	}
}