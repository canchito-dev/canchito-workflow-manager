package com.canchitodev.cwm.security;

import org.flowable.rest.security.SecurityConstants;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.env.Environment;
import org.springframework.http.HttpMethod;
import org.springframework.security.authentication.AuthenticationProvider;
import org.springframework.security.config.annotation.method.configuration.EnableGlobalMethodSecurity;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.config.annotation.web.configuration.WebSecurityConfigurerAdapter;

@Configuration
@EnableGlobalMethodSecurity(prePostEnabled = true) //The prePostEnabled property enables Spring Security pre/post annotations
@EnableWebSecurity
public class CWMWebSecurityConfigurationAdapter extends WebSecurityConfigurerAdapter {
	
	@Autowired
    protected Environment environment;
	
	@Bean
    public AuthenticationProvider authenticationProvider() {
        return new BasicAuthenticationProvider();
    }
	
    protected void configure(HttpSecurity http) throws Exception {
    	http.authenticationProvider(authenticationProvider()).csrf().disable()
    		.authorizeRequests()
    		.antMatchers(HttpMethod.POST, "/process-api/repository/deployments").hasAuthority(SecurityConstants.ACCESS_ADMIN)
    		.anyRequest().authenticated()
    		.and().httpBasic();
    }
}